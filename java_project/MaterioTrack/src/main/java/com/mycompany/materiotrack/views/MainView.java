    private class ToolbarActionListener implements ActionListener {
        private final String section;

        public ToolbarActionListener(String section) {
            this.section = section;
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            cardLayout.show(contentPanel, section);
        }
    }

    private class NavButtonListener implements ActionListener {
        private final String panelName;

        public NavButtonListener(String panelName) {
            this.panelName = panelName;
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            if (currentNavButton != null) {
                currentNavButton.setBackground(new Color(60, 60, 60));
            }
            currentNavButton = (JButton) e.getSource();
            currentNavButton.setBackground(new Color(0, 102, 204));
            
            CardLayout cl = (CardLayout) contentPanel.getLayout();
            cl.show(contentPanel, panelName);
        }
    }
   
